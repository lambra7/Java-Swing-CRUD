    public void updateGrid(){
        limpaGrid();
        try {
            String caminhoAbsoluto = new File("").getAbsolutePath();
            FileReader fr = new FileReader(caminhoAbsoluto + "\\produtos.txt");
            BufferedReader reader = new BufferedReader(fr);
            String linha = reader.readLine();
            while (linha != null) {
                //[nome][ncm][valor][aliq]
                String[] dados = linha.split("!@!");
                Produto prod = new Produto();
                prod.setNome(dados[0]);
                prod.setNcm(dados[1]);
                prod.setValor(dados[2]);
                prod.setAliq(dados[3]);
                
                escreveNaGrid(prod);
                linha = reader.readLine();
            }

        } catch (Exception e) {
            System.out.println("erro na leitura");
        }
    }
    
    public void limpaGrid(){
        DefaultTableModel tabela= (DefaultTableModel)tblProdutos.getModel();
        int count = tabela.getRowCount();
        for (int i = count; i > 0; i--) {
            tabela.removeRow(i-1);
        }
        tblProdutos.setModel(tabela);
    }
    
    public void escreveNaGrid(Produto prd){
        
        DefaultTableModel tabela=(DefaultTableModel)tblProdutos.getModel(); // tirar dúvidas sobre a estrutura do default
        tabela.addRow(new Object[]{prd.getNome(),prd.getNcm(),prd.getValor(),prd.getAliq()}); // cria uma nova linha no vetor de objetos e pega os dados inseridos nos campos
            
    }
    
    public void salvaNoArquivo(Produto prd){
        try {
            String breakline = System.getProperty("line.separator");
            String caminhoAbsoluto = new File("").getAbsolutePath();
            FileWriter escritor = new FileWriter(caminhoAbsoluto + "\\produtos.txt", true);
            escritor.write(prd.getNome() + "!@!" + prd.getNcm() + "!@!" + prd.getValor() + "!@!" + prd.getAliq());
            escritor.write(breakline);
            escritor.close();
        } catch (Exception e) {
            System.out.println("Erro na escrita do arquivo");
            e.printStackTrace();
        }
    }
    
    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        lblmensagem.setText("");
        DefaultTableModel tabela=(DefaultTableModel) tblProdutos.getModel();
        if(tblProdutos.getSelectedRow()==-1){
            if(tblProdutos.getRowCount()==0){
                lblmensagem.setText("Tabela sem produtos cadastrados!");
                JOptionPane.showMessageDialog(null, "Tabela sem produtos cadastrados!");
            }else{
                lblmensagem.setText("Selecione um produto para atualizar!");
                JOptionPane.showMessageDialog(null, "Selecione um produto para atualizar");
            }
        }else{
            //1 - ler a lista que na ta no arquivo
            List<Produto> produtos = lerLista();
            //2 - atualiza linha da lista criando um novo objeto e substituindo pelo antigo
            Produto prod = new Produto();
            prod.setNome(txtNome.getText());
            prod.setNcm(boxNcm.getSelectedItem().toString());
            prod.setValor(txtValor.getText());
            prod.setAliq(txtAliq.getText());
            int i = tblProdutos.getSelectedRow();
            produtos.set(i,prod);
            
//            tabela.setValueAt(txtNome.getText(), tblProdutos.getSelectedRow(), 0); // os números são posição no vetor
//            tabela.setValueAt(boxNcm.getSelectedItem().toString(), tblProdutos.getSelectedRow(), 1); // os números são posição no vetor
//            tabela.setValueAt(txtValor.getText(),tblProdutos.getSelectedRow(), 2); // os números são posição no vetor
//            tabela.setValueAt(txtAliq.getText(), tblProdutos.getSelectedRow(), 3); // os números são posição no vetor
            //3.1 - limpa arquivo
            limpaArquivo();
            //3.2 - escreve um por um
            for (Produto produto : produtos) {
                salvaNoArquivo(produto);
            }
            //4 - update grid
            updateGrid();
            JOptionPane.showMessageDialog(null, "Item atualizado com sucesso!");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

