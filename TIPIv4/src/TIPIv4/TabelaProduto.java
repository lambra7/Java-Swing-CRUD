    public void limpaArquivo(){
        try {
            String breakline = System.getProperty("line.separator");
            String caminhoAbsoluto = new File("").getAbsolutePath();
            FileWriter escritor = new FileWriter(caminhoAbsoluto + "\\produtos.txt", false);
            escritor.write("");
            escritor.close();
        } catch (Exception e) {
            System.out.println("Erro na escrita do arquivo");
            e.printStackTrace();
        }
    }
    
    public List<Produto> lerLista(){
        List<Produto> produtos = new ArrayList<>();
        try {
            String caminhoAbsoluto = new File("").getAbsolutePath();
            FileReader fr = new FileReader(caminhoAbsoluto + "\\produtos.txt");
            BufferedReader reader = new BufferedReader(fr);
            String linha = reader.readLine();
            Produto prod;
            while (linha != null) {
                //[nome][ncm][valor][aliq]
                String[] dados = linha.split("!@!");
                prod = new Produto();
                prod.setNome(dados[0]);
                prod.setNcm(dados[1]);
                prod.setValor(dados[2]);
                prod.setAliq(dados[3]);
                produtos.add(prod);
                linha = reader.readLine();
            }

        } catch (Exception e) {
            System.out.println("erro na leitura da lista");
        }
        return produtos;
    }
    
    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        DefaultTableModel tabela=(DefaultTableModel) tblProdutos.getModel();
        if(tblProdutos.getSelectedRow()==-1){
            if(tblProdutos.getRowCount()==0){
                lblmensagem.setText("Tabela sem produtos cadastrados!");
                JOptionPane.showMessageDialog(null, "Tabela sem produtos cadastrados!");
            }else{
                lblmensagem.setText("Selecione um produto para deletar!");
                JOptionPane.showMessageDialog(null, "Selecione um produto para deletar");
            }
        }else{
            //tabela.removeRow(tblProdutos.getSelectedRow());
            //1 - ler a lista que na ta no arquivo
            List<Produto> produtos = lerLista();
            //2 - remove linha da lista
            produtos.remove(tblProdutos.getSelectedRow());
            //3.1 - limpa arquivo
            limpaArquivo();
            //3.2 - escreve um por um
            for (Produto produto : produtos) {
                salvaNoArquivo(produto);
            }
            //4 - update grid
            updateGrid();
            JOptionPane.showMessageDialog(null, "Item excluído com sucesso!");
            
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

